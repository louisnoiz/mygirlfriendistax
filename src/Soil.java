public class Soil {
    private int loca_x;
    private int loca_y;

    public Soil(int x, int y) {
        this.loca_x = x;
        this.loca_y = y;
    }

    public int getLoca_x() {
        return loca_x;
    }

    public void setLoca_x(int loca_x) {
        this.loca_x = loca_x;
    }

    public int getLoca_y() {
        return loca_y;
    }

    public void setLoca_y(int loca_y) {
        this.loca_y = loca_y;
    }
}
